// 1. Abstração: Focar no que é importante, ignorando detalhes desnecessários.
// 2. Encapsulamento: Esconder detalhes de implementação e mostrar apenas o necessário.
// 3. Herança (prototype): Reaproveitar código e estabelecer relações entre objetos.
// 4. Polimorfismo: Capacidade de um objeto se comportar de diferentes formas, dependendo do contexto.